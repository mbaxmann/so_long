#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	tr;

	tr = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = n * (-1);
		}
		if (n < 10)
		{
			n = 48 + n;
			write(fd, &n, 1);
		}
		else
		{
			tr = n / 10;
			n = n % 10;
			ft_putnbr_fd(tr, fd);
			ft_putnbr_fd(n, fd);
		}
	}
}
